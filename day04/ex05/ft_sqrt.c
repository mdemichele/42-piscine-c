int	ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (i * i < 2147483647 && i * i <= nb)
	{
		while (i * i <= nb)
		{
			if (i * i == nb)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
