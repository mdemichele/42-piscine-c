int	ft_find_next_prime(int nb)
{
	int i;
	int next;

	i = 2;
	next = nb;
	if (nb < 2)
		return (2);
	while (i * i <= nb)
	{
		if (nb % i == 0)
		{
			i = 2;
			nb++;
		}
		else
			i++;
	}
	return (nb);
}
