int	ft_add(int a, int b)
{
	return (a + b);
}

int	ft_subtract(int a, int b)
{
	return (a - b);
}

int	ft_divide(int a, int b)
{
	return (a / b);
}

int	ft_multiply(int a, int b)
{
	return (a * b);
}

int	ft_modulus(int a, int b)
{
	return (a % b);
}
