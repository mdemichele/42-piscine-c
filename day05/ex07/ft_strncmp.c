int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (i < n)
	{
		if (*s2 == '\0')
			return (1);
		if (*s2 > *s1)
			return (-1);
		if (*s2 < *s1)
			return (1);
		s1++;
		s2++;
	}
	if (*s2 != '\0')
		return (-1);
	return (0);
}
